
/*Programação orientada a objetos e programação estruturada
na programação estruturada: um programa é composta por três tipos básicos de estrutura:
  Sequências: são comandos a serem executados;
  Condições: sequências só são executadas se uma condição for satisfeita (exemplo: if-else, swtich)
  Repetições: sequências que devem ser executadas repetidamente até uma condição ser satisfeita(exemplo: for, while, do-while) 
    Essas estruturas são usadas para realizar a entrada do programa, alterando dados até que a saída esperada seja gerada, nessa parte a programação orientada a objetos
e a estruturada são iguais.
    Na linguagem estruturada um programa é geralmente escrito em uma única rotina(ou função) podendo ser quebrado em várias subrotinas. Mas o fluxo do programa continua
o mesmo, de forma que suas subrotinas podem ser copiadas para as rotinas que as chamam de forma que no final haja apenas uma rotina que execute todo o programa.
    O acesso às variáveis não possuem muitas restrições na programação estruturada, restringir o acesso à uma variável nesse tipo de linguagem se limita a dizer se
ela é visível ou não dentro de uma função, mas não se pode dizer de forma nativa que uma variável pode ser acessada apenas por algumas rotinas do programa. Um contorno
para isso é o uso de variáveis globais, prática que pode ser danosa ao desenvolvimento do sistema. Variáveis globais são usadas tipicamente para manter estados no
programa, marcando em qual parte a execussão se encontra.
    A programação orientada a objetos surgiu como uma alternativa à esses problemas. O nome objeto vem do intuito de que esse tipo de linguagem tenta se aproximar do 
manuseio de coisas tangíveis.
    Esse novo paradigma se baseia principalmente em dois conceitos chave: Classes e Objetos.
    Classe: É uma forma de definir um objeto do mundo real na forma de linguagem de programação. Formados por dados(atributos) e comportamentos(métodos)
     Exemplo: classe: mamíferos 
  */ 

